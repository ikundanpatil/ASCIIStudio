function renderWith(map: Record<string, string[]>, height: number, text: string): string {
  const upper = text.toUpperCase();
  const rows: string[] = Array(height).fill("");
  for (const ch of upper) {
    const glyph = map[ch] ?? map[" "];
    for (let i = 0; i < height; i++) {
      rows[i] += (glyph[i] ?? "") + " ";
    }
  }
  return rows.join("\n");
}

